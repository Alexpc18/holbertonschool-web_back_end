#!/usr/bin/env python3
"""
This function takes a float as input and
returns its closest minor integer value.
"""

import math


def floor(n: float) -> int:
    """
    Return the floor of a float.

    Args:
        n (float): The float to find the floor of.

    Returns:
        int: The floor value of the float.
    """
    return math.floor(n)
