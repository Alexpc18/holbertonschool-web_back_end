#!/usr/bin/env python3
"""
We define a function called 'to_str' that takes a floating number 'n'
as input and returns its string representation using the 'str()' function.
"""


def to_str(n: float) -> str:
    """
    Convert a float to its string representation.

    Parameters:
        n (float): The float to be converted to a string.

    Returns:
        str: The string representation of the float.
    """
    return str(n)
